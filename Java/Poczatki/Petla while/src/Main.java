public class Main {
    public static void main(String[] args) {

        // petla while
        int w=0;
        while (w<5){
            System.out.println(w);
            w++;
        }
        System.out.println();

        //petla for
        for (int f = 0; f < 5; f++) {
            System.out.println(f);
        }
        System.out.println();

        //petla do while

        int d=0;
        do {
            System.out.println(d);
            d++;
        }while (d<5);
    }
}